def cuentas():
    n1 = int(input("Ingrese un valor entero: "))
    n2 = int(input("Ingrese otro valor entero: "))
    print("El valor de la suma es: ", n1 + n2)
    print("El valor de la resta es: ", n1 - n2)
    print("El valor de la multiplicacion es: ", n1 * n2)
    print("El valor de la division es: ", n1 / n2)
    print("El valor de la division entera es: ", n1 // n2)
cuentas()
